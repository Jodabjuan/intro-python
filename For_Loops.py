"""
Practice For loops
Keyword: for
"""

cities = ["London", "New York", "Paris", "Ogden", "Madrid"]
#Iterate over this list
for city in cities:
    print(city)


d = {'alice':'801-123-4567',
     'pedro':'435-594-2541',
     'john': '215-564-6584'}
#Iterate over this Dictionary
for item in d:
    print(item)
    print(d[item])
